# Dictionary

grade = {'pey':10, 'julliet':99}
print(grade['pey'])
print(grade['julliet'])

dict = {'name':'Suyeon',"age":25,'birth':1024}
print(dict['name'])
print(dict['age'])
print(dict['birth'])

dict2 = {'name':'Suyeon',"age":25,'birth':1024, 'name':'Suyoung'}
print(dict2)

